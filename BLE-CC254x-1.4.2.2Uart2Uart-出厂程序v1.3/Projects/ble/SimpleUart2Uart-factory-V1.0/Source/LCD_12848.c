void IO_LCD_Init(void)
{
  /*配置成输出模式*/
  IO_LcdBL_DDR  |= IO_LcdBL_BV;
  IO_LcdRES_DDR |= IO_LcdRES_BV;
  IO_LcdA0_DDR  |= IO_LcdA0_BV;;
  IO_LcdSCL_DDR |= IO_LcdSCL_BV;
  I2CWC = 0x82;//将I2C引脚设置成普通IO口使用
  
  HAL_OFF_IO_LcdBL();
  HAL_OFF_IO_LcdRES();
  HAL_OFF_IO_LcdA0();
  HAL_OFF_IO_LcdSCL();
  IO_LcdSDA_L;
}
void Write_Com(uint8 para)
{
    uint8 j;
    j        = 8;
//    CS       = 0;
    IO_LcdA0 = 0;
    do
    {
      if(para&0x80)IO_LcdSDA_H;
      else         IO_LcdSDA_L;
      IO_LcdSCL   = 0;
      IO_LcdSCL   = 1;
      --j;
      para<<=1;
    }
    while(j);
    //    CS       = 1;
}
void Write_Data(uint8 para)
{
    uint8 j;
    j        = 8;
    //    CS       = 0;
    IO_LcdA0 = 1;
    do
    {
      if(para&0x80)IO_LcdSDA_H; 
      else         IO_LcdSDA_L;
      IO_LcdSCL   = 0;
      IO_LcdSCL   = 1;
      --j;
      para<<=1;
    }
      while(j);
    //    CS       = 1;
}
void DelayMS(uint16 ms)//32MHz
{ 
    uint16 a;
    while(ms)
    {
      a=1500;
      while(a--);
      ms--;
    }
}

