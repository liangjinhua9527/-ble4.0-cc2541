{
  uint8 i,j,k,l;
  uint8 o = 23;
   if(line == 1)
   {
      k = 0xb0;
      l = 0xb2;
   }
   else if(line == 2)
   {
      k = 0xb2;
      l = 0xb4;
   }
   else if(line == 3)
   {
      k = 0xb4;
      l = 0xb6;        
   }
   else 
   {
      k = 0;
      l = 0;  
   }
   for(i=k;i<l;i++)
   {
     Write_Com(i);
     Write_Com((col>>4) | 0x10);
     Write_Com(col & 0x0f);

     for(j=0;j<12;j++)
     {

       Write_Data(*(*font+o));
       o--;
     }
    }
}
